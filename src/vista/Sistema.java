    private void btnActualizarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarClienteActionPerformed
    // Verifica si el campo de ID del cliente está vacío
    if ("".equals(txtIdCliente.getText())) {
        JOptionPane.showMessageDialog(null, "Seleccione una fila");
    } else {
        // Verifica si todos los campos necesarios están llenos
        if (!"".equals(txtDNICliente.getText()) && !"".equals(txtNombreCliente.getText())
                && !"".equals(txtTelefonoCliente.getText()) && !"".equals(txtDireccionCliente.getText())) {
            try {
                // Setea los valores del cliente
                cl.setDni(Integer.parseInt(txtDNICliente.getText()));
                cl.setNombre(txtNombreCliente.getText());
                cl.setTelefono(txtTelefonoCliente.getText());
                cl.setDireccion(txtDireccionCliente.getText());
                cl.setId(Integer.parseInt(txtIdCliente.getText()));
                // Llama al método para modificar el cliente
                boolean actualizado = cliente.ModificarCliente(cl);
                if (actualizado) {
                    JOptionPane.showMessageDialog(null, "Cliente actualizado correctamente");
                    LimpiarTabla();
                    LimpiarCliente();
                    ListarCliente();
                } else {
                    JOptionPane.showMessageDialog(null, "Error al actualizar el cliente");
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Error en el formato de los datos: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(null, "Los campos están vacíos");
        }
    }
    }//GEN-LAST:event_btnActualizarClienteActionPerformed

    private void btnGuardarClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarClienteActionPerformed
    try {
        if (!"".equals(txtDNICliente.getText().trim()) && !"".equals(txtNombreCliente.getText().trim())
                && !"".equals(txtTelefonoCliente.getText().trim()) && !"".equals(txtDireccionCliente.getText().trim())) {

            int dni = Integer.parseInt(txtDNICliente.getText().trim());
            String telefonoStr = txtTelefonoCliente.getText().trim();
            
            // Validar que el teléfono tenga exactamente 10 dígitos y sea un número
            if (telefonoStr.length() != 10 || !telefonoStr.matches("\\d{10}")) {
                JOptionPane.showMessageDialog(null, "El teléfono debe tener exactamente 10 dígitos numéricos");
                return;
            }

            cl.setDni(dni);
            cl.setNombre(txtNombreCliente.getText().trim());
            cl.setTelefono(telefonoStr);
            cl.setDireccion(txtDireccionCliente.getText().trim());
            cliente.registrarCliente(cl);
            LimpiarTabla();
            LimpiarCliente();
            ListarCliente();
            JOptionPane.showMessageDialog(null, "Cliente registrado");
        } else {
            JOptionPane.showMessageDialog(null, "Todos los campos deben ser llenados");
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(null, "DNI debe ser un número válido");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Ocurrió un error al registrar el cliente: " + e.getMessage());
    }

    }//GEN-LAST:event_btnGuardarClienteActionPerformed

